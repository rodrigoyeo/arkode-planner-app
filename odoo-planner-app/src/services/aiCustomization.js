/**
 * Check if AI customization is available
 * @returns {Object} { available: boolean, provider: 'claude'|'openai'|null }
 */
export function getAICustomizationStatus() {
  const { provider } = detectProvider();
  return {
    available: !!provider,
    provider: provider
  };
}
